// Usage: node scripts/generate-content-etags.mjs
// Walks functions/api/content/_data/{gradedStories,grammarAdvanced}.js,
// computes SHA-1 of each resource's stable JSON, writes _etags.js.
import { writeFile, access } from 'node:fs/promises';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, resolve } from 'node:path';
import { createHash } from 'node:crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA_DIR = resolve(__dirname, '..', 'functions', 'api', 'content', '_data');

function stableStringify(value) {
  if (value === null || typeof value !== 'object') return JSON.stringify(value);
  if (Array.isArray(value)) return '[' + value.map(stableStringify).join(',') + ']';
  const keys = Object.keys(value).sort();
  return '{' + keys.map((k) => JSON.stringify(k) + ':' + stableStringify(value[k])).join(',') + '}';
}

export async function computeEtag(resource) {
  return createHash('sha1').update(stableStringify(resource)).digest('hex');
}

async function fileExists(p) {
  try {
    await access(p);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  const storiesPath = resolve(DATA_DIR, 'gradedStories.js');
  const grammarAdvancedPath = resolve(DATA_DIR, 'grammarAdvanced.js');
  const grammarPath = resolve(DATA_DIR, 'grammar.js');
  const lessonsPath = resolve(DATA_DIR, 'lessons.js');
  const corePath = resolve(DATA_DIR, 'core.js');

  for (const p of [storiesPath, grammarAdvancedPath, grammarPath, lessonsPath, corePath]) {
    if (!(await fileExists(p))) {
      throw new Error(`[generate-content-etags] missing required data file: ${p}`);
    }
  }

  const storiesMod = await import(pathToFileURL(storiesPath).href);
  const grammarAdvancedMod = await import(pathToFileURL(grammarAdvancedPath).href);
  const grammarMod = await import(pathToFileURL(grammarPath).href);

  const GRADED_STORIES = storiesMod.GRADED_STORIES || storiesMod.default;
  const ADVANCED_UNITS = grammarAdvancedMod.ADVANCED_UNITS || grammarAdvancedMod.default;

  if (!Array.isArray(GRADED_STORIES)) {
    throw new Error('[generate-content-etags] GRADED_STORIES not an array');
  }
  if (!Array.isArray(ADVANCED_UNITS)) {
    throw new Error('[generate-content-etags] ADVANCED_UNITS not an array');
  }

  const stories = {};
  for (const s of GRADED_STORIES) {
    if (!s?.id) throw new Error('[generate-content-etags] story missing id');
    stories[s.id] = await computeEtag(s);
  }

  const grammarUnits = {};
  for (const u of ADVANCED_UNITS) {
    if (!u?.id) throw new Error('[generate-content-etags] grammar unit missing id');
    grammarUnits[u.id] = await computeEtag(u);
  }

  // SP11b: hash all grammar exports as a single composite resource.
  // VERBS (conjugation curriculum dataset) MUST be included so the grammar etag
  // changes when verb data changes — otherwise cached clients 304 and never see it.
  const grammarExports = {
    PADEZI: grammarMod.PADEZI,
    GRAM: grammarMod.GRAM,
    CONJ: grammarMod.CONJ,
    MODAL: grammarMod.MODAL,
    TENSES: grammarMod.TENSES,
    ASPECT: grammarMod.ASPECT,
    ASPECT_PAIRS: grammarMod.ASPECT_PAIRS,
    CONDITIONAL: grammarMod.CONDITIONAL,
    FORMAL_REGISTER: grammarMod.FORMAL_REGISTER,
    IMPERSONAL: grammarMod.IMPERSONAL,
    PHONOLOGY: grammarMod.PHONOLOGY,
    PITCH_ACCENT: grammarMod.PITCH_ACCENT,
    PADEZI_FULL: grammarMod.PADEZI_FULL,
    VERBS: grammarMod.VERBS,
  };
  const grammar = await computeEtag(grammarExports);

  const lessonsMod = await import(pathToFileURL(lessonsPath).href);
  const LESSONS = lessonsMod.LESSONS || lessonsMod.default;
  if (!Array.isArray(LESSONS)) {
    throw new Error('[generate-content-etags] LESSONS not an array');
  }
  const lessons = await computeEtag(LESSONS);

  const coreMod = await import(pathToFileURL(corePath).href);
  // SP11d + SP11e: 27 high-IP-density exports. LEARN_PATH (97 items, ckRule JSON DSL)
  // and SEASONAL_CAMPAIGNS (4 entries, windowKind discriminator) added in SP11e
  // — both now serialize cleanly via the function/data split documented in
  // docs/superpowers/specs/2026-05-16-sp11e-final-closure-design.md.
  const CORE_KEYS = ['V','COUNTRIES','PROFESSIONS','WEATHER','CLOTHES','BODYDESC','TECH_VOC','BUREAUCRATIC','PROVERBS','IDIOMS','BRZALICE','HISTORY','EVENTS','KINGS','REGIONS','DIALECTS','CROATIAN_CITIES','FOODORDER','TRANSPORT','GROCERY','RECIPES','PRACTICAL','SCENES','LEVEL_NARRATIVE','SHADOWING','LEARN_PATH','SEASONAL_CAMPAIGNS','V_B2','V_C1'];
  const coreExports = {};
  for (const k of CORE_KEYS) {
    if (coreMod[k] === undefined) throw new Error(`[generate-content-etags] core.js missing export: ${k}`);
    coreExports[k] = coreMod[k];
  }
  const core = await computeEtag(coreExports);

  const catalog = await computeEtag({ stories, grammarUnits, grammar, lessons, core });

  const out = `// AUTO-GENERATED by scripts/generate-content-etags.mjs - do not edit by hand.
export const ETAGS = ${JSON.stringify({ stories, grammarUnits, grammar, lessons, core, catalog }, null, 2)};
`;
  await writeFile(resolve(DATA_DIR, '_etags.js'), out, 'utf8');
  console.log(`[generate-content-etags] wrote ${Object.keys(stories).length} stories + ${Object.keys(grammarUnits).length} grammar units + grammar(${grammar.slice(0, 8)}...) + ${LESSONS.length} lessons(${lessons.slice(0, 8)}...) + core(${core.slice(0, 8)}...)`);
}

const isMain = process.argv[1] ? import.meta.url === pathToFileURL(process.argv[1]).href : false;
if (isMain) {
  main().catch((e) => {
    console.error(e);
    process.exit(1);
  });
}
